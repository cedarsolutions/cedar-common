package com.cedarsolutions.dao.gae.impl;

import com.cedarsolutions.dao.gae.IFilterPredicate;

/**
 * Filter predicate that discards entities with ids greater than 100.
 * @author Kenneth J. Pronovici <pronovic@ieee.org>
 */
public class NoLargeIdFilterPredicate implements IFilterPredicate<IntegerIdEntity> {

    @Override
    public boolean evaluate(IntegerIdEntity value) {
        return !(value != null && value.getId() > 100);
    }

}
