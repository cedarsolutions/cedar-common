import com.mvp4g.client.event.EventBusWithLookup;

/**
 * Event bus that is aware of client session events.
 * @author Kenneth J. Pronovici <pronovic@ieee.org>
 */
public interface SessionAwareEventBus extends EventBusWithLookup {

    /** Initialize the session for this module. */
    void initializeSession(String eventType, Object[] params);

    /** Clear the session for this module. */
    void clearSession();

}
